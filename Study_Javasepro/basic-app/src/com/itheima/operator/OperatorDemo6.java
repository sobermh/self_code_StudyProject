package com.itheima.operator;

public class OperatorDemo6 {
    public static void main(String[] args) {
        int a =10;
        int b =10;
        boolean rs= a==b;
        System.out.println(rs);
        System.out.println(a != b);
        System.out.println(a > b);
        System.out.println(a >= b);

        int i =10;
        int j =5;
        boolean rs1= i==j;
        System.out.println(rs1);
        System.out.println(i != j);
        System.out.println(i > j);
        System.out.println(i >= j);

    }
}

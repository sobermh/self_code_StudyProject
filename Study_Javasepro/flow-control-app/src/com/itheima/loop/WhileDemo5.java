package com.itheima.loop;

public class WhileDemo5 {
    public static void main(String[] args) {
        int i =0;
        while(i<3){
            System.out.println("HelloWorld");
            i++;
        }

        System.out.println("-----------------------------");
        int j =0;
        while(j<3){
            System.out.println("HelloWorld");
            j++;
        }

    }
}
